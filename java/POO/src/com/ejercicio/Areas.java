package com.ejercicio;

public class Areas {
	 public double areaCuadrado(int l) {
	        return l*l;
	    }
	    
	    public double areaTriangulo(int base, int altura) {
	        return (base * altura) / 2;
	    }
	    
	    public double areaCirculo(int r) {
	        return Math.PI*(r*r);
	    }

}
